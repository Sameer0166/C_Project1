int main(){
    int i=5;
    printf("%d\n",i--);
    printf("%d",i);
    /*    This program demonstrates the use of the post-decrement operator in C.
    It initializes an integer variable 'i' to 5, prints its value using the post    
decrement operator, and then prints the value of 'i' again after the decrement.
    The post-decrement operator (i--) decreases the value of 'i' by 1       
after its current value has been used in the expression.
    This is a simple demonstration of how to use the post-decrement operator in C.
    The program is useful for understanding how the post-decrement operator works,
    */
    return 0;
}     